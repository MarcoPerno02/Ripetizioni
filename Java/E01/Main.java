public class Main {             // Il nome del file deve corrispondere al nome della classe
                                // Inoltre si utilizza la notazione Camel Case

    // Ogni classe è consigliabile abbia una funzione chiamata main,
    // indipendentemente dal nome della classe.
    // La funzione main sarà quella funzione eseguita come prima cosa quando
    // si esegue un file java.
    public static void main(String[] args) {
        stampaHelloWorld();
    }

    public static void stampaHelloWorld() {
        System.out.println("Hello, World!");
    } 
}
